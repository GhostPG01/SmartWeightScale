uint32_t ADC_ReadValue(void)
{
    uint32_t adcValue = 0;

    HAL_ADC_Start(&hadc1);
    if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK)
    {
        adcValue = HAL_ADC_GetValue(&hadc1);
    }
    HAL_ADC_Stop(&hadc1);

    return adcValue;
}

// 将 ADC 采样数据转换为对应的电压值
float ADC_ConvertToVoltage(uint32_t adcValue)
{
    return ((float)adcValue / ADC_RESOLUTION) * VREF;
}

